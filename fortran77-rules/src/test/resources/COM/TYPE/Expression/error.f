 PROGRAM ESSAI

C..   Formal Arguments .. 
      double precision APX
      double precision AKPX
      integer IND
      logical ERREUR
C 
C..   Local Scalars .. 
      integer I,IM
      logical FIN_TRAITEMENT
      double precision epsilon
      save epsilon
C 
C..   Local Arrays .. 
      double precision AKP(28),AP(28)
	  STOP
	  
	  APX = APX + AKPX
	  APX = IND + AKPX
	  AKPX = epsilon * I
	  I = AP ** IM 
	  I = I ** ERREUR 
	   
  END PROGRAM
